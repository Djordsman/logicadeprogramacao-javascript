// Adição (+)
let resultado = 57 + 83;

// Subtração (-)
resultado = 5 - 3;

// Multiplicação (*)
resultado = 10 * 3;

// Divisão (/)
resultado = 10 / 2.5;

// Módulo (%) - Pega o resto da divisão do primeiro valor pelo segundo.
resultado = 15 % 4;

// Incremento (++) serve para adicionar +1 unidade no calculo, poupando uma linha de comando extensa. Ex extenso: "numero = numero +1".
let numero = 10;
numero++;

// Decremento: o mesmo conceito do incremento, porém inverso.
numero = 20
numero--;

// Exponenciação (**) > Eleva o primeiro valor a potência do segundo.
let valor = 2 ** 4 // (2 elevado à potencia de 4)

console.log(valor);